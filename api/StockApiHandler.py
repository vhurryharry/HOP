class StockApiHandler(Resource):
    def get(self, symbol):
        timestamps = defaultTS
        prices = defaultPrices
        predictions = []

        return {
            'symbol': symbol,
            'timestamps': timestamps,
            'prices': prices,
            'predictions': predictions
        }

    def get_real(self, symbol):
        url = "https://yfapi.net/v8/finance/chart/{}".format(symbol)
        queryString = {"range": "10y",
                       "interval": "1d",
                       "indicators": "quote"}
        headers = {
            'x-api-key': os.environ['YAHOO_KEY']
        }

        response = requests.request(
            'GET', url, headers=headers, params=queryString)

        if response.status_code == requests.codes.ok:
            data = response.json()

            timestamps = data["chart"]["result"][0]["timestamp"]
            prices = data["chart"]["result"][0]["indicators"]["adjclose"][0]["adjclose"]
            predictions = []
        else:
            timestamps = []
            prices = []
            predictions = []

        return {
            'symbol': symbol,
            'timestamp': timestamps,
            'prices': prices,
            'predictions': predictions
        }
